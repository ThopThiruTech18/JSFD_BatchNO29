package no01.operators;

public class ArithematicOperators {
	
	
	
	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);//10/20==> 10 , 19/20==> 19,21/20==> 1,201/20=1
		
		
		
		
	}

}
