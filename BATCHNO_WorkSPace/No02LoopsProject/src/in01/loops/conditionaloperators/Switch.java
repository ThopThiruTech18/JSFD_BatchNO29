package in01.loops.conditionaloperators;

public class Switch {
	public static void main(String[] args) {
		
		int bulbNo=7;
		switch (bulbNo) {
		case 1:System.out.println("Bulb Glow in room no-1");
		break;
		case 2:System.out.println("Bulb Glow in room no-2 : ");
		break;
		case 3:System.out.println("Bulb Glow in room no-3 : ");
		break;
		case 4:System.out.println("Bulb Glow in room no-4 : ");
		break;
		case 5:System.out.println("Bulb Glow in room no-5 : ");
		break;
		case 6:System.out.println("Bulb Glow in room no-6 : ");
		default: System.out.println("Please enter a valid room number");
		}
		
		
		
		
		
	}

}
